     public void tampilkanData(){
        String[] kolom={"ID","Nama","Alamat","Telepon"};
        ArrayList<member>list =new member().getAll();
        Object rowData[]=new Object[4];
        
        tblAnggota.setModel(new DefaultTableModel(new Object[][] {}, kolom));
        for(member mbr:list){
            rowData[0]=mbr.getIdanggota();
            rowData[1]=mbr.getNama();
            rowData[2]=mbr.getAlamat();
            rowData[3]=mbr.getTelepon();
            ((DefaultTableModel)tblAnggota.getModel()).addRow(rowData);
        }
     }
      public void cari(String keyword){
        String[] kolom={"ID","Nama","Alamat","Telepon"};
        ArrayList<member>list=new member().search(keyword);
        Object rowData[]=new Object[4];
        
        tblAnggota.setModel(new DefaultTableModel(new Object[][] {},kolom));
        for(member mbr:list){
            rowData[0]=mbr.getIdanggota();
            rowData[1]=mbr.getNama();
            rowData[2]=mbr.getAlamat();
            rowData[3]=mbr.getTelepon();
            
            ((DefaultTableModel)tblAnggota.getModel()).addRow(rowData);
        }
    }
