    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
        member mbr=new member();
        mbr.setIdanggota(Integer.parseInt(txtIdAnggota.getText()));
        mbr.setNama(txtNama.getText());
        mbr.setAlamat(txtAlamat.getText());
        mbr.setTelepon(txtTelp.getText());
        mbr.save();
        txtIdAnggota.setText(Integer.toString(mbr.getIdanggota()));
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model=(DefaultTableModel)tblAnggota.getModel();
        int row=tblAnggota.getSelectedRow();
        
        member mbr=new member().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        mbr.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

