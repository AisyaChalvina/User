    public void tampilkanData(){
        String[] kolom={"ID","IdAnggota","IdBuku","TanggalPinjam","TanggalKembali"};
        ArrayList<peminjaman> list =new peminjaman().getAll();
        Object rowData[]=new Object[5];
     tblPem.setModel(new DefaultTableModel(new Object[][] {},kolom));
     for(int i=0;i<list.size();i++){
            rowData[0]=list.get(i).getIdPeminjaman();
            rowData[1]=list.get(i).getAnggota().getIdanggota();
            rowData[2]=list.get(i).getBuku().getIdbuku();
            rowData[3]=list.get(i).getTanggalPinjam();
            rowData[4]=list.get(i).getTanggalKembali();
            ((DefaultTableModel)tblPem.getModel()).addRow(rowData);
        }
    }
     public void cari(String keyword){
        String[] kolom={"ID","IdAnggota","IdBuku","TanggalPinjam","TanggalKembali"};
        ArrayList<peminjaman>list=new peminjaman().search(keyword);
        Object rowData[]=new Object[5];
        
        tblPem.setModel(new DefaultTableModel(new Object[][] {},kolom));
        for(peminjaman pem:list){
           rowData[0]=pem.getIdPeminjaman();
            rowData[1]=pem.getAnggota().getNama();
            rowData[2]=pem.getBuku();
            rowData[3]=pem.getTanggalPinjam();
            rowData[4]=pem.getTanggalKembali();
            ((DefaultTableModel)tblPem.getModel()).addRow(rowData);
        }
        }   
     public void cariAnggota(String keyword){
       new member().getById(Integer.parseInt(keyword));
       lblNamaAnggota.setText(new member().getById(Integer.parseInt(keyword)).getNama());
    }
    public void cariBuku(String keyword){
       new books().getById(Integer.parseInt(keyword));
       lblJudulBuku.setText(new books().getById(Integer.parseInt(keyword)).getJudul());
    }
