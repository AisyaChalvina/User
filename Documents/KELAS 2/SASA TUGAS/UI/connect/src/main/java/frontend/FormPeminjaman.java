    private void tblPemMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblPemMouseClicked
       DefaultTableModel model = (DefaultTableModel)tblPem.getModel();
        int row = tblPem.getSelectedRow();
        peminjaman pmjn = new peminjaman();
        
        pmjn = pmjn.getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        
        txtID.setText(String.valueOf(pmjn.getIdPeminjaman()));
        txtIdAnggota.setText(String.valueOf(pmjn.getAnggota()));
        txtIdBuku.setText(String.valueOf(pmjn.getBuku()));
        txttp.setText(pmjn.getTanggalPinjam());
        txttk.setText(pmjn.getTanggalKembali());
    }//GEN-LAST:event_tblPemMouseClicked

    private void btnSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSimpanActionPerformed
       peminjaman pmjn = new peminjaman();
        member anggota = new member();
        books buku = new books();
        pmjn.setIdPeminjaman(Integer.parseInt(txtID.getText()));
        pmjn.setAnggota(anggota.getById(Integer.parseInt(txtIdAnggota.getText())));
        pmjn.setBuku(buku.getById(Integer.parseInt(txtIdBuku.getText())));
        pmjn.setTanggalPinjam(txttp.getText());
        pmjn.setTanggalKembali(txttk.getText());
        pmjn.save();
        
        txtID.setText(Integer.toString(pmjn.getIdPeminjaman()));
        
        tampilkanData();
    }//GEN-LAST:event_btnSimpanActionPerformed

    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        DefaultTableModel model=(DefaultTableModel)tblPem.getModel();
        int row=tblPem.getSelectedRow();
        
        peminjaman p=new peminjaman().getById(Integer.parseInt(model.getValueAt(row, 0).toString()));
        p.delete();
        kosongkanForm();
        tampilkanData();
    }//GEN-LAST:event_btnHapusActionPerformed

